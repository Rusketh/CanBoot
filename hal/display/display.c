/*
 * HAL display backed by the loader-provided framebuffer. The drawing
 * primitives match the cando script-facing surface exposed by the
 * cando display lib (cando_port/lib/display.c).
 *
 * Pixel encoding from callers is 0x00RRGGBB; we encode to the device
 * pitch + channel mask discovered at boot, so the same script lines
 * paint the same colours whether GOP gave us 32-bpp BGRX or RGBX.
 */

#include <stdint.h>
#include <stddef.h>
#include <string.h>

#include "canboot/boot_info.h"
#include "hal/display.h"

static const struct canboot_fb *g_fb;
static uint32_t g_red_shift, g_green_shift, g_blue_shift;

void canboot_display_bind(const struct canboot_fb *fb) {
    if (!fb || fb->format != CANBOOT_FB_RGB || fb->bpp != 32) {
        g_fb = NULL;
        return;
    }
    g_fb = fb;
    g_red_shift   = fb->red_mask_shift;
    g_green_shift = fb->green_mask_shift;
    g_blue_shift  = fb->blue_mask_shift;
}

bool hal_display_init(void)       { return g_fb != NULL; }
bool hal_display_present(void)    { return g_fb != NULL; }
int32_t hal_display_width(void)   { return g_fb ? (int32_t)g_fb->width  : 0; }
int32_t hal_display_height(void)  { return g_fb ? (int32_t)g_fb->height : 0; }

static inline uint32_t encode_pixel(uint32_t rgb) {
    uint32_t r = (rgb >> 16) & 0xFFu;
    uint32_t g = (rgb >> 8)  & 0xFFu;
    uint32_t b =  rgb        & 0xFFu;
    return (r << g_red_shift) | (g << g_green_shift) | (b << g_blue_shift);
}

static inline uint32_t decode_pixel(uint32_t raw) {
    uint32_t r = (raw >> g_red_shift)   & 0xFFu;
    uint32_t g = (raw >> g_green_shift) & 0xFFu;
    uint32_t b = (raw >> g_blue_shift)  & 0xFFu;
    return (r << 16) | (g << 8) | b;
}

static inline volatile uint32_t *fb_row(int32_t y) {
    return (volatile uint32_t *)((volatile uint8_t *)(uintptr_t)g_fb->addr +
                                 (uintptr_t)y * g_fb->pitch);
}

void hal_display_clear(uint32_t color) {
    if (!g_fb) return;
    uint32_t raw = encode_pixel(color);
    for (uint32_t y = 0; y < g_fb->height; y++) {
        volatile uint32_t *row = fb_row((int32_t)y);
        for (uint32_t x = 0; x < g_fb->width; x++) row[x] = raw;
    }
}

void hal_display_fill_rect(int32_t x0, int32_t y0,
                           int32_t w, int32_t h, uint32_t color) {
    if (!g_fb || w <= 0 || h <= 0) return;
    int32_t x1 = x0 + w, y1 = y0 + h;
    if (x0 < 0) x0 = 0;  if (y0 < 0) y0 = 0;
    if (x1 > (int32_t)g_fb->width)  x1 = (int32_t)g_fb->width;
    if (y1 > (int32_t)g_fb->height) y1 = (int32_t)g_fb->height;
    uint32_t raw = encode_pixel(color);
    for (int32_t y = y0; y < y1; y++) {
        volatile uint32_t *row = fb_row(y);
        for (int32_t x = x0; x < x1; x++) row[x] = raw;
    }
}

void hal_display_pixel(int32_t x, int32_t y, uint32_t color) {
    if (!g_fb || x < 0 || y < 0 ||
        x >= (int32_t)g_fb->width || y >= (int32_t)g_fb->height) return;
    fb_row(y)[x] = encode_pixel(color);
}

uint32_t hal_display_get_pixel(int32_t x, int32_t y) {
    if (!g_fb || x < 0 || y < 0 ||
        x >= (int32_t)g_fb->width || y >= (int32_t)g_fb->height) return 0;
    return decode_pixel(fb_row(y)[x]);
}

void hal_display_line(int32_t x0, int32_t y0,
                      int32_t x1, int32_t y1, uint32_t color) {
    if (!g_fb) return;
    /* Bresenham. */
    int32_t dx = (x1 > x0) ? (x1 - x0) : (x0 - x1);
    int32_t dy = (y1 > y0) ? -(y1 - y0) : -(y0 - y1);
    int32_t sx = x0 < x1 ? 1 : -1;
    int32_t sy = y0 < y1 ? 1 : -1;
    int32_t err = dx + dy;
    uint32_t raw = encode_pixel(color);
    int32_t W = (int32_t)g_fb->width, H = (int32_t)g_fb->height;
    while (1) {
        if (x0 >= 0 && y0 >= 0 && x0 < W && y0 < H)
            fb_row(y0)[x0] = raw;
        if (x0 == x1 && y0 == y1) break;
        int32_t e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}

void hal_display_image(int32_t x, int32_t y, int32_t w, int32_t h,
                       const uint32_t *src) {
    if (!g_fb || !src || w <= 0 || h <= 0) return;
    for (int32_t row = 0; row < h; row++) {
        int32_t yy = y + row;
        if (yy < 0 || yy >= (int32_t)g_fb->height) continue;
        volatile uint32_t *frow = fb_row(yy);
        for (int32_t col = 0; col < w; col++) {
            int32_t xx = x + col;
            if (xx < 0 || xx >= (int32_t)g_fb->width) continue;
            frow[xx] = encode_pixel(src[row * w + col]);
        }
    }
}

void hal_display_copy_rect(int32_t sx, int32_t sy,
                           int32_t dx, int32_t dy,
                           int32_t w, int32_t h) {
    if (!g_fb || w <= 0 || h <= 0) return;
    /* Choose row iteration order to avoid clobber when src/dst overlap. */
    bool top_down = dy <= sy;
    for (int32_t row = 0; row < h; row++) {
        int32_t r = top_down ? row : (h - 1 - row);
        int32_t sy_r = sy + r, dy_r = dy + r;
        if (sy_r < 0 || sy_r >= (int32_t)g_fb->height) continue;
        if (dy_r < 0 || dy_r >= (int32_t)g_fb->height) continue;
        volatile uint32_t *srow = fb_row(sy_r);
        volatile uint32_t *drow = fb_row(dy_r);
        bool left_to_right = dx <= sx;
        for (int32_t col = 0; col < w; col++) {
            int32_t c = left_to_right ? col : (w - 1 - col);
            int32_t sxc = sx + c, dxc = dx + c;
            if (sxc < 0 || sxc >= (int32_t)g_fb->width) continue;
            if (dxc < 0 || dxc >= (int32_t)g_fb->width) continue;
            drow[dxc] = srow[sxc];
        }
    }
}

