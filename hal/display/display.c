void hal_display_text(int32_t x, int32_t y, const char *str,
                      uint32_t fg, uint32_t bg) {
    if (!g_fb || !str) return;
    for (const char *p = str; *p; p++, x += 8) {
        char c = *p;
        if (c < 0x20 || c > 0x7E) continue;
        const uint8_t *gl = font8x8[(int)(c - 0x20)];
        for (int row = 0; row < 8; row++) {
            int32_t yy = y + row;
            if (yy < 0 || yy >= (int32_t)g_fb->height) continue;
            volatile uint32_t *frow = fb_row(yy);
            uint8_t bits = gl[row];
            for (int col = 0; col < 8; col++) {
                int32_t xx = x + col;
                if (xx < 0 || xx >= (int32_t)g_fb->width) continue;
                frow[xx] = encode_pixel((bits & (1 << col)) ? fg : bg);
            }
        }
    }
}
